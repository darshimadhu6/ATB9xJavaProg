package Oct.ex_21102024;

public class Lab024_forloopEven {
    public static void main(String[] args) {
        for (int i = 0; i <= 50; i++) {
            if (i%2 ==0){
                System.out.println("Even ->"+i);
            }
            else{
                System.out.println("Odd ->"+i);
            }

        }
    }
}
